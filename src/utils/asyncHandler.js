// A WRAPPER FUNCTION THAT WE WILL BE USING ON MANY PLACES
// TO LEARN MORE ABOUT PROMISE AND THIS BELOW SYNTAX

const asyncHandler = (requestHandler) => {
  (req, res, next) => {
    Promise.resolve(requestHandler(req, res, next)).catch((err) => next(err));
  };
};

export { asyncHandler };

